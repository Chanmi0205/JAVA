
public class Variable1 {
	
	public static void main(String[] args) {
		int level;  // 정수형 변수 level을 선언
		level = 10; // level 변수에 값 10을 대입
		System.out.println("현재 레벨은 : "+ level); // level값 출력
		
		int number1 = 100;
		double number2 = 116.3324;
		int number3 = (int) (number1 + number2);
		
		int number4 = 100;
		double number5 = 116.3324;
		
		System.out.println(number3);
		System.out.println(number4 + number5);
		
		/* 참 거짓 데이터 : boolean

		 * 정수 데이터 = int

		 * 실수 데이터 : double

		 * 문자열 데이터 = String  */
		
	}

}
