
public class Main2 {
	
	public static void main(String[] args) {
		
		int a = 10;
		System.out.println("현재의 a는  " + a + "입니다.");
		System.out.println("\n");
		
		a++; // ++ : 증감연산자
		
		System.out.println("현재의 a는  " + a + "입니다.");
		System.out.println("현재의 a는  " + ++a + "입니다."); //더한 다음 출력함
		System.out.println("현재의 a는  " + a++ + "입니다."); //출력한 후 더함
		System.out.println("현재의 a는  " + a + "입니다.");
	}

}
